    let currentLanguage = localStorage.getItem('darkPatternLang') || 'ja';

    function t(key) {
      return (translations[currentLanguage] && translations[currentLanguage][key]) || translations.ja[key] || key;
    }

    function setLanguage(lang) {
      currentLanguage = lang;
      localStorage.setItem('darkPatternLang', lang);
      
      // Update all language buttons
      document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
      });
      
      // Update all elements with data-text attribute
      document.querySelectorAll('[data-text]').forEach(el => {
        const key = el.dataset.text;
        if (translations[lang][key]) {
          el.textContent = translations[lang][key];
        }
      });
      
      // Update all elements with data-html attribute
      document.querySelectorAll('[data-html]').forEach(el => {
        const key = el.dataset.html;
        if (translations[lang][key]) {
          el.innerHTML = translations[lang][key];
        }
      });
      
      // Update game translations and re-render if game exists
      if (window.game && typeof window.game.updateTranslations === 'function') {
        window.game.updateTranslations();
      }
      if (window.game && window.game.render) {
        window.game.render();
      }
    }

    // Initialize language button listeners
    function initLanguageButtons() {
      document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', () => {
          setLanguage(btn.dataset.lang);
        });
      });
      
      // Set initial language state
      document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === currentLanguage);
      });
    }
